package com.ibm.automation;

public class Constant {

	public String name;
	public String constantLabel;
	public String pluralConstantLabel;
	
	public Constant()  {
		
	}
			
	public Constant(String name, String constantLabel, String pluralConstantLabel) {
		this.name = name;
		this.constantLabel = constantLabel;
		this.pluralConstantLabel = pluralConstantLabel;
	}

}
